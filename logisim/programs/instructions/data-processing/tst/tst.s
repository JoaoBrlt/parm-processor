// Two positive values.
// ...10010001 AND ...00110110 = ...00010000
// Flags = 0000
MOVS r0, #145
MOVS r1, #54
TST r1, r0

// Two opposite sign values.
// 11111111 11111111 11111111 11111111 AND
// 00000000 00000000 00000000 10101010 =
// 00000000 00000000 00000000 10101010
// Flags = 0000
MOVS r2, #0
MVNS r2, r2
MOVS r3, #170
TST r3, r2

// Two negative values.
// 11111111 11111111 11111111 00100001 AND
// 11111111 11111111 11111111 11111111 =
// 11111111 11111111 11111111 00100001
// Flags = 1000
MOVS r4, #222
MVNS r4, r4
MOVS r5, #0
MVNS r5, r5
TST r5, r4

// Same register.
// ...00001010 AND ...00001010 = ...00001010
// Flags = 0000
