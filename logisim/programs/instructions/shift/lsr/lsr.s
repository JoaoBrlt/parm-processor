// Shift by a bit.
// 11111111 11111111 11111111 01010101 >> 00001 =
// 01111111 11111111 11111111 10101010
MOVS r0, #170
MVNS r0, r0
LSRS r1, r0, #1

// Shift by seven bits.
// 11111111 11111111 11111111 01010101 >> 00111 =
// 00000001 11111111 11111111 11111110
MOVS r2, #170
MVNS r2, r2
LSRS r3, r2, #7

// Shift by a byte.
// 11111111 11111111 11111111 11111111 >> 01000 =
// 00000000 11111111 11111111 11111111
MOVS r4, #0
MVNS r4, r4
LSRS r5, r4, #8

// Shift by three bytes.
// 11111111 11111111 11111111 00110011 >> 11000 =
// 00000000 00000000 00000000 11111111
MOVS r6, #204
MVNS r6, r6
LSRS r7, r6, #24

// Shift itself by a bit.
// 11111111 11111111 11111111 01111110 >> 00001 =
// 01111111 11111111 11111111 10111111
MOVS r0, #129
MVNS r0, r0
LSRS r0, r0, #1

// Shift itself by maximum.
// 11111111 11111111 11111111 00000000 >> 11111 =
// 00000000 00000000 00000000 00000001
MOVS r1, #255
MVNS r1, r1
LSRS r1, r1, #31

// Shift itself by over maximum.
// 11111111 11111111 11111111 00000000 >> 00000 =
// 11111111 11111111 11111111 00000000
MOVS r2, #255
MVNS r2, r2
LSRS r2, r2, #32
